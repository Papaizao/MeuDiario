    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
    
        
        try 
        {
            // Busca aluno e professor pelo ID. Caso não encontre, cairá na excessão
           this.alunoDAO.getAluno(Integer.parseInt(txtRA.getText()));
            this.professorDAO.getProfessor(Integer.parseInt(txtId.getText()));  
                
            Aluno a = new Aluno();
            Professor p = new Professor();
            Classe c = new Classe();

            a.setRA(Integer.parseInt(txtRA.getText()));
            p.setId(Integer.parseInt(txtId.getText()));
            c.setTurma(txtTurma.getText());
            
            this.classeDAO.inserir(c.getTurma(),p.getId(), a.getRA());
            
            JOptionPane.showMessageDialog(this, "Classe criada com sucesso");
            limparCampos();
            preencherTabela();
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao inserir: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        
        try {
            
            this.alunoDAO.getAluno(Integer.parseInt(txtRA.getText()));
            this.professorDAO.getProfessor(Integer.parseInt(txtId.getText()));        
           
                
            Aluno a = new Aluno();
            Professor p = new Professor();
            Classe c = new Classe();

            a.setRA(Integer.parseInt(txtRA.getText()));
            p.setId(Integer.parseInt(txtId.getText()));
            c.setTurma(txtTurma.getText());
            c.setMedia(Double.parseDouble(txtMedia.getText()));
            
            if(c.getMedia()>10)
            {                
                JOptionPane.showMessageDialog(this, "Nota máxima permitida é 10");
                return;
            }
            else if(c.getMedia()<0)
            {                
                JOptionPane.showMessageDialog(this, "Nota invalida");
                return;
            }
            this.classeDAO.atualizar(c.getMedia(), c.getTurma());
            JOptionPane.showMessageDialog(this, "Classe atualizada com sucesso");            
            habilitarSalvar(true);            
            preencherTabela();
            limparCampos();
            habilitarCampo(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }       
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tblClasse.getSelectedRow();
        if(linhaSelecionada == -1){
            JOptionPane.showMessageDialog(this, "Selecione uma classe na tabela");
        }
        else{
            //pega o ID do aluno selecionado
            String turma = (String) tblClasse.getValueAt(linhaSelecionada, 0);
            
            try {
                Classe c = (Classe) this.classeDAO.getClass(turma);
                
                txtRA.setText(String.valueOf(c.getRA()));
                txtTurma.setText(turma); 
                txtId.setText(String.valueOf(c.getId()));
                
                habilitarSalvar(false);
                habilitarCampo(true);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }            
        }
    }//GEN-LAST:event_btnCarregarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         int confirma = JOptionPane.showConfirmDialog(this, "Deseja realmente excluir?");
        if(confirma == 0) {
            String turma = txtTurma.getText();
            
            try {
                this.classeDAO.deletar(turma);
                JOptionPane.showMessageDialog(this, "Classe excluída com sucesso");
                limparCampos();
                habilitarSalvar(true);
                habilitarCampo(false);
                preencherTabela();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

