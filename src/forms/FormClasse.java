    private void preencherTabela() 
    {
        DefaultTableModel tabela = (DefaultTableModel) tblClasse.getModel();
        tabela.setNumRows(0);
        
        try
        {
            List<Classe> lista;       
            if(txtBuscar.getText().equals(""))
            {
                lista = this.classeDAO.getClasses();
            }
            else
            {
                lista = this.classeDAO.getClasse(txtBuscar.getText());                
            }
            for(Classe c : lista) 
            {
                Object[] novaLinha = new Object[]{
                    c.getTurma(),
                    c.getRA(),
                    c.getId(),                    
                    c.getMedia()
                };                
                tabela.addRow(novaLinha);
            }
        } 
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, "Erro ao preencher tabela: " + e.getMessage());
        }        
    }
    
